'use strict';

/**
 * merge-flat-eslint-config.js
 *
 * Adds the perfect-typescripter rule block to a flat ESLint config.
 *
 * For new flat configs, writes a fresh `eslint.config.js` from a
 * template. For existing flat configs, takes the safe path: writes a
 * sibling file `eslint.config.perfect-typescripter.js` and instructs
 * the user to compose it via `import` + spread. We do not AST-rewrite
 * the user's existing config — flat configs are JavaScript and can be
 * arbitrarily dynamic (function exports, async, conditional spreads).
 * Pretending to safely merge would break configs we cannot statically
 * understand.
 */

const fs = require('fs');
const path = require('path');

const FRESH_FLAT_CONFIG_TEMPLATE = `// @generated by perfect-typescripter /setup-eslint
// Edit the rule severities below if any rule is too noisy on your codebase.
// To remove perfect-typescripter, delete this block and uninstall the dep.

const tsParser = require('@typescript-eslint/parser');
const perfectTypescripter = require('eslint-plugin-perfect-typescripter');

module.exports = [
  {
    files: ['**/*.ts', '**/*.tsx'],
    languageOptions: {
      parser: tsParser,
      parserOptions: { ecmaVersion: 'latest', sourceType: 'module' },
    },
    plugins: { 'perfect-typescripter': perfectTypescripter },
    rules: {
      'perfect-typescripter/no-phantom-type-param': 'error',
      'perfect-typescripter/no-duplicate-envelope-shape': 'warn',
      'perfect-typescripter/no-shared-variant-literal-across-discriminated-unions': 'warn',
      'perfect-typescripter/no-variant-prefix-drift': 'warn',
    },
  },
];
`;

const SIBLING_FLAT_CONFIG_TEMPLATE = `// @generated by perfect-typescripter /setup-eslint
// SIBLING CONFIG — your project already has a flat ESLint config.
// We did not modify it because we cannot safely merge into a JS export
// that may be dynamic. Compose this block into your main config:
//
//   const perfectTs = require('./eslint.config.perfect-typescripter.js');
//   module.exports = [...yourExistingArray, ...perfectTs];
//
// Or if your config is a function export, return the spread inside it.

const tsParser = require('@typescript-eslint/parser');
const perfectTypescripter = require('eslint-plugin-perfect-typescripter');

module.exports = [
  {
    files: ['**/*.ts', '**/*.tsx'],
    languageOptions: {
      parser: tsParser,
      parserOptions: { ecmaVersion: 'latest', sourceType: 'module' },
    },
    plugins: { 'perfect-typescripter': perfectTypescripter },
    rules: {
      'perfect-typescripter/no-phantom-type-param': 'error',
      'perfect-typescripter/no-duplicate-envelope-shape': 'warn',
      'perfect-typescripter/no-shared-variant-literal-across-discriminated-unions': 'warn',
      'perfect-typescripter/no-variant-prefix-drift': 'warn',
    },
  },
];
`;

function writeFreshFlatConfig(projectRoot) {
  const target = path.join(projectRoot, 'eslint.config.js');
  if (fs.existsSync(target)) {
    return { action: 'skipped-existing', path: target };
  }
  fs.writeFileSync(target, FRESH_FLAT_CONFIG_TEMPLATE);
  return { action: 'wrote-fresh', path: target };
}

function writeSiblingFlatConfig(projectRoot) {
  const target = path.join(projectRoot, 'eslint.config.perfect-typescripter.js');
  const alreadyExists = fs.existsSync(target);
  fs.writeFileSync(target, SIBLING_FLAT_CONFIG_TEMPLATE);
  return { action: alreadyExists ? 'wrote-sibling-overwrite' : 'wrote-sibling-new', path: target };
}

function alreadyMerged(projectRoot) {
  const flatPath = path.join(projectRoot, 'eslint.config.js');
  if (!fs.existsSync(flatPath)) return false;
  try {
    const content = fs.readFileSync(flatPath, 'utf-8');
    return content.includes('eslint-plugin-perfect-typescripter') || content.includes('perfect-typescripter/no-phantom-type-param');
  } catch {
    return false;
  }
}

module.exports = { writeFreshFlatConfig, writeSiblingFlatConfig, alreadyMerged };
